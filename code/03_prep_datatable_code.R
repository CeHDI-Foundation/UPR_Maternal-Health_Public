  rename(skilled_birth_year_min = YEAR_min, skilled_birth_year_max = YEAR_max) |> 
  # filter(num_years >= 5) |>
  select(COUNTRY, skilled_birth_max, skilled_birth_min, skilled_birth_change, skilled_birth_log_change, 
         skilled_birth_year_min, skilled_birth_year_max, num_years)

