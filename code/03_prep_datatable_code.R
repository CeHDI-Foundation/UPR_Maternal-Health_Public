n_sup_2_long <- sdg_data |> 
  filter(! cycle %in% c(
    "Cycle 4"
    # , "Cycle 3"
  )) |> 
  # filter(state_under_review %in% c(mmr_most$country_name)) |> 
  mutate(state_under_review = factor(state_under_review)) |> 
  # filter(response_upr %in% c("Supported", "Noted")) |> 
  group_by(state_under_review, response_upr) |> 
  summarise(across(c(health_related:other_health_related), ~ sum(.x != "Other"))) |> 
  ungroup() |>
  filter(response_upr %in% c("Supported", "Noted")) |>
  pivot_longer(
    cols = health_related:other_health_related,
    names_to = "theme",
    values_to = "n"
  ) |> 
  arrange(state_under_review, theme, response_upr) |> 
  group_by(state_under_review, theme) |> 
  mutate(
    n_tot = sum(n),
    perc = round(n/n_tot*100,1)
  ) |> 
  ungroup() |> 
  filter(response_upr == "Supported") |> 
  group_by(theme) |> 
  mutate(n_tot_median = ceiling(median(n_tot[n_tot!=0]))) |> 
  ungroup() |> 
  mutate(
    recs_cat = factor(case_when(
      n_tot == 0 ~ "0",
      n_tot >= 4 ~ "4+",
      n_tot < 4 ~ "1-3"
    )),
    support_cat = factor(case_when(
      perc >= 80 ~ "80+",
      perc <80 ~ "<80"),
      levels = c("80+", "<80")),
    
    recs_median = factor(case_when(
      n_tot == 0 ~ "0",
      n_tot <= n_tot_median ~ paste0("1-",n_tot_median),
      n_tot > n_tot_median ~ paste0(n_tot_median+1,"+")
    )
    # ,levels = c("Fewer recommendations", 
    #            "More recommendations",
    #            "No recommendations")
    ),
    recs_median = fct_recode(recs_median, "1" = "1-1"),
    combined = factor(case_when(
      n_tot == 0 ~ "No recommendations",
      n_tot < n_tot_median & perc <80 ~ "Fewer recommendations, <80% support",
      n_tot < n_tot_median & perc >=80 ~ "Fewer recommendations, 80+% support",
      n_tot >= n_tot_median & perc <80 ~ "More recommendations, <80% support",
      n_tot >= n_tot_median & perc >=80 ~ "More recommendations, 80+% support"
    ),
    levels = c("More recommendations, 80+% support", "More recommendations, <80% support", 
               "Fewer recommendations, 80+% support", "Fewer recommendations, <80% support",
               "No recommendations")
    )
  ) |> 
  group_by(theme) |> 
  mutate(
    perc_c = perc-mean(perc, na.rm = TRUE),
    perc_c = case_when(is.na(perc_c) ~ 0, .default = perc_c)
  ) |> 
  ungroup() |> 
  left_join(theme_labels, join_by(theme == variable)) |> 
  rename(n_supported = n) |> 
  select(-c(response_upr))

